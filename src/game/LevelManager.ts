export const getLevel = (stageNum: number): LevelData => {
  const index = Math.max(1, Math.min(STAGES.length, stageNum)) - 1;
  const stage = STAGES[index];
  return {
    ...stage,
    grid: stage.grid ? [...stage.grid] : undefined,
    maps: stage.maps ? stage.maps.map(m => ({
      ...m,
      grid: [...m.grid]
    })) : undefined,
  };
};
